                <h1 style="text-align: start">Quantum Monte Carlo</h1>
                <p>The emergence of a variety of methods that allow one to investigate the behaviour of strongly
                    correlated systems has, in the last half of the century, created a new branch of physical research
                    that works as a complement to other, strongly entrenched, techniques. Because of the quantum
                    mechanical stamp and the size of the Hilbert spaces, the complexity that has arisen in the
                    constantly evolving world description separates the possible methods, forcing the scientist to
                    carefully choose one fitting the needs of the theory or the experiment. One of the most prominent
                    ones, the Quantum Monte Carlo approach, seems to be a fine route for examining fragments of the
                    parameter space in a stochastic manner, where the analytic description has not yet found, or
                    possibly may even never find, a reasonable solution. With the usage of models, one can indeed seek
                    to simulate a vast amount of novel materials such as low- and high-temperature superconductors,
                    heavy-fermion materials, or even materials of exotic topological character. During the workshops, an
                    elementary introduction to the world of Quantum Monte Carlo approaches will be presented and, with
                    the focus on the DQMC method, the participants would then be able spot the dozing power of the
                    approach and to understand the fundamental problems one has to stomp upon afoot.</p>
